public class Alumno {
    int id;
    String nombre;
    String apellido1;
    String apellido2;
    double promedio;

    public Alumno(int id, String nombre, String apellido1, String apellido2, double promedio) {
        this.id = id;
        this.nombre = nombre;
        this.apellido1 = apellido1;
        this.apellido2 = apellido2;
        this.promedio = promedio;
    }
}